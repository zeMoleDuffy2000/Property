
class Property:
    """A sample property class"""

    def __init__(self, address1, address2, desc, price, propertyType, bedroomNo, bathroomNo, tenure, link):
        self.address1 = address1
        self.address2 = address2
        self.desc = desc
        self.price = price
        self.propertyType = propertyType
        self.bedroomNo = bedroomNo
        self.bathroomNo = bathroomNo
        self.tenure = tenure
        self.link = link

